package th.in.ffc.util;

import android.content.Context;
import android.content.Intent;
import android.net.Uri;


public class MarketAccess {
    private Context mContext;
    public static String PACKAGE = "th.or.nectec.ffc.familytree";

    public MarketAccess(Context context) {
        mContext = context;
    }

    public void openMarketActivity() {
        Intent intent = new Intent(Intent.ACTION_VIEW);
        intent.setData(Uri.parse("market://details?id=" + PACKAGE));
        mContext.startActivity(intent);
    }
}
